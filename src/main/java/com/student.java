package com;

public class student {


        String id ;
        String name;
        String birth;

        public String getId() {
                return id;
        }

        public void setId(String id) {
                this.id = id;
        }

        public String getName() {
                return name;
        }

        public void setName(String name) {
                this.name = name;
        }

        public String getBirth() {
                return birth;
        }

        public void setBirth(String birth) {
                this.birth = birth;
        }
}
